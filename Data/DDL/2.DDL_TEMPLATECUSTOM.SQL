
  CREATE TABLE TEMPLATECUSTOM 
   (
	TEMPLATECUSTOMID NUMBER(11,0) NOT NULL, 
	TEMPLATEID NUMBER(11,0) NOT NULL , 
	COMPANYSITEID NUMBER(11,0) NOT NULL , 
	USERID NUMBER(11,0) NOT NULL , 
	CUSTOMNAME VARCHAR2(255) NOT NULL , 
	TEMPLATESTATUSID NUMBER(11,0) NOT NULL , 
	DATECREATED DATE DEFAULT SYSDATE, 
	DATEUPDATED DATE DEFAULT SYSDATE, 
	CONSTRAINT TEMPLATECUSTOM_PK PRIMARY KEY (TEMPLATECUSTOMID)
   ); 