
  CREATE TABLE TEMPLATEFIELDCLOB 
   (	TEMPLATEFIELDID NUMBER(11,0) NOT NULL , 
	TEMPLATECUSTOMID NUMBER(11,0) DEFAULT 0, 
	FREECLOB CLOB, 
	 CONSTRAINT TEMPLATEFIELDCLOB_PK PRIMARY KEY (TEMPLATECUSTOMID, TEMPLATEFIELDID), 
	 CONSTRAINT TEMPLATEFIELDCLOB_TF_FK FOREIGN KEY (TEMPLATEFIELDID)
	  REFERENCES CUSTOMER.TEMPLATEFIELD (TEMPLATEFIELDID) , 
	 CONSTRAINT TEMPLATEFIELDCLOB_TC_FK FOREIGN KEY (TEMPLATECUSTOMID)
	  REFERENCES CUSTOMER.TEMPLATECUSTOM (TEMPLATECUSTOMID) 
   );